# Оголошуємо функцію-декоратор, яка приймає іншу функцію як аргумент
def validate_positive(func):
    # Визначаємо внутрішню функцію-обгортку, яка буде виконуватися замість оригінальної
    def wrapper(*args, **kwargs):
        # Об'єднуємо всі позиційні та іменовані аргументи у спільний список для перевірки
        all_args = list(args) + list(kwargs.values())

        # Перебираємо всі аргументи, які передані у функцію
        for arg in all_args:
            # Перевіряємо: якщо аргумент є числом (int або float) і він недодатній (<=0)
            if isinstance(arg, (int, float)) and arg <= 0:
                # Викидаємо помилку, щоб припинити виконання функції
                raise ValueError(f"Аргумент {arg} не є додатнім числом!")

        # Якщо всі аргументи додатні — викликаємо оригінальну функцію з тими ж аргументами
        return func(*args, **kwargs)

    # Повертаємо внутрішню функцію wrapper — саме вона замінює оригінальну функцію
    return wrapper
