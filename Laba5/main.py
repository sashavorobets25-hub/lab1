# Імпортуємо наш декоратор із файлу decorator.py
from decorator import validate_positive

# Декоруємо функцію multiply — тепер перед її виконанням аргументи будуть перевірятися
@validate_positive
def multiply(a, b):
    # Повертаємо добуток двох чисел
    return a * b

# Декоруємо іншу функцію, щоб перевірити роботу декоратора з трьома аргументами
@validate_positive
def add_three_numbers(a, b, c):
    # Повертаємо суму трьох чисел
    return a + b + c

# Перевіряємо, що код запускається лише тоді, коли файл виконується напряму (а не імпортується)
if __name__ == "__main__":
    # Викликаємо функцію multiply з додатніми числами → все працює нормально
    print(multiply(3, 5))          # Очікуваний результат: 15

    # Викликаємо add_three_numbers з додатніми числами → також усе ок
    print(add_three_numbers(2, 4, 8))  # Очікуваний результат: 14

    # Тепер пробуємо передати від’ємне число, щоб перевірити, як працює перевірка
    try:
        # multiply(3, -2) має викликати помилку, бо -2 не є додатнім
        print(multiply(3, -2))
    # Обробляємо помилку, якщо декоратор її викине
    except ValueError as e:
        # Виводимо повідомлення про помилку замість аварійного завершення програми
        print("Помилка:", e)
