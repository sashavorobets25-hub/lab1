# Імпортуємо декоратор із файлу decorator.py
from decorator import validate_positive

# Декоруємо функцію multiply — перед виконанням будуть перевірятися аргументи
@validate_positive
def multiply(a, b):
    # Повертаємо добуток двох чисел
    return a * b

# Декоруємо іншу функцію — перевірка працюватиме і для 3 аргументів
@validate_positive
def add_three_numbers(a, b, c):
    # Повертаємо суму трьох чисел
    return a + b + c

# Блок, що виконується тільки коли файл запускається напряму
if __name__ == "__main__":
    # Викликаємо функцію multiply з додатніми числами → все працює нормально
    print(multiply(3, 5))            # Очікуваний вивід: 15

    # Викликаємо add_three_numbers з додатніми числами → також усе ок
    print(add_three_numbers(2, 4, 8))  # Очікуваний вивід: 14

    # Тепер пробуємо передати від’ємне число, щоб перевірити роботу перевірки
    try:
        # multiply(3, -2) має викликати помилку, бо -2 не є додатнім
        print(multiply(3, -2))
    except ValueError as e:
        # Обробляємо помилку і друкуємо пояснення замість аварійного завершення
        print("Помилка:", e)
